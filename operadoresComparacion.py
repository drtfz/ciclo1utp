resultado = a != b  # diferente
print(resultado)

resultado = a < b  # menor
print(resultado)

resultado = a <= b  # menor o igual
print(resultado)

resultado = a > b  # mayor
print(resultado)

resultado = a >= b  # mayor o igual
print(resultado)
